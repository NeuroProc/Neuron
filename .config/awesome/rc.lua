-- {{{ Wibox
function percent_color(value, reverse)
  if value < 30 then
    if reverse then 
      color = beautiful["percent_high"]
    else
      color = beautiful["percent_low"]
    end
  elseif value < 70 then
    color = beautiful["percent_middle"]
  else
    if reverse then 
      color = beautiful["percent_low"] 
    else 
      color = beautiful["percent_high"] 
    end
  end
  return "<span color='" .. color .. "'>" .. value .. "</span>"
end

